struct parser_imp
{
	parser_imp(std::istream& data, parser& parser);

	~parser_imp();

	// Here comes the parser part
	void parse(bool validate);

	// the productions. Some are inlined below for obvious reasons.
	// names of the productions try to follow those in the TR http://www.w3.org/TR/xml
	void prolog();
	void xml_decl();
	void text_decl();

	void s(bool at_least_one = false);
	void eq();
	void misc();
	void element(doctype::validator& valid);
	void content(doctype::validator& valid, bool check_for_whitespace);

	void comment();
	void pi();

	void pereference();

	void doctypedecl();
	data_source* get_data_source(const std::string& pubid, std::string uri);
	std::tuple<std::string, std::string> read_external_id();
	void intsubset();
	void extsubset();
	void declsep();
	void conditionalsect();
	void ignoresectcontents();
	void markup_decl();
	void element_decl();
	void contentspec(doctype::element& element);
	doctype::allowed_ptr cp();
	void attlist_decl();
	void notation_decl();
	void entity_decl();
	void parameter_entity_decl();
	void general_entity_decl();
	void entity_value();

	// at several locations we need to parse out entity references from strings:
	void parse_parameter_entity_declaration(std::string& s);
	void parse_general_entity_declaration(std::string& s);

	// same goes for attribute values
	std::string normalize_attribute_value(const std::string& s)
	{
		m_source.emplace(new string_data_source(s));
		return normalize_attribute_value();
	}

	std::string normalize_attribute_value();

	// The scanner is next. We recognize the following tokens:
	enum XMLToken
	{
		xml_Undef,
		xml_Eof = 256,

		// these are tokens for the markup

		xml_XMLDecl,  // <?xml
		xml_Space,	// Really needed
		xml_Comment,  // <!--
		xml_Name,	 // name-start-char (name-char)*
		xml_NMToken,  // (name-char)+
		xml_String,   // (\"[^"]*\") | (\'[^\']*\')		// single or double quoted std::string
		xml_PI,		  // <?
		xml_STag,	 // <
		xml_ETag,	 // </
		xml_DocType,  // <!DOCTYPE
		xml_Element,  // <!ELEMENT
		xml_AttList,  // <!ATTLIST
		xml_Entity,   // <!ENTITY
		xml_Notation, // <!NOTATION

		xml_IncludeIgnore, // <![

		xml_PEReference, // %name;

		// next are tokens for the content part

		xml_Reference, // &name;
		xml_CDSect,	// CData section <![CDATA[ ... ]]>
		xml_Content,   // anything else up to the next element start
	};

	// for debugging and error reporting we have the following describing routine
	std::string describe_token(int token);

	unicode get_next_char();

	// Recognizing tokens differs if we are expecting markup or content in elements:
	int get_next_token();
	int get_next_content();

	// retract is used when we've read a character too much from the input stream
	void retract();

	// match, check if the look-a-head token is really what we expect here.
	// throws if it isn't. Depending on the content flag we call either get_next_token or get_next_content
	// to find the next look-a-head token.
	void match(int token);

	// utility routine
	float parse_version();

	// error handling routines
	void not_well_formed(const std::string& msg) const;
	// void			not_well_formed(const boost::format& msg) const			{ not_well_formed(msg.str()); }
	void not_valid(const std::string& msg) const;
	// void			not_valid(const boost::format& msg) const				{ not_valid(msg.str()); }

	// doctype support
	const doctype::entity& get_general_entity(const std::string& name) const;
	const doctype::entity& get_parameter_entity(const std::string& name) const;
	const doctype::element *get_element(const std::string& name) const;

	const doctype::entity& get_general_entity(const std::wstring& name) const
	{
		return get_general_entity(wstring_to_string(name));
	}

	const doctype::entity& get_parameter_entity(const std::wstring& name) const
	{
		return get_parameter_entity(wstring_to_string(name));
	}

	const doctype::element *get_element(const std::wstring& name) const
	{
		return get_element(wstring_to_string(name));
	}

	// Sometimes we need to reuse our parser/scanner to parse an external entity e.g.
	// We use stack based state objects to store the current state.
	struct parser_state
	{
		float m_version = 1.0f;
		encoding_type m_encoding = encoding_type::enc_UTF8;
		bool m_external_subset = true;
		bool m_allow_parameter_entity_references = true;
		bool m_in_external_dtd = false;
		bool m_in_doctype = false; // used to keep track where we are (parameter entities are only recognized inside a doctype section)
		bool m_in_element = false;
		bool m_in_content = false;
	};

	void push_state()
	{
		m_state_stack.push(m_state);
	}

	void pop_state()
	{
		assert(not m_state_stack.empty());
		m_state = m_state_stack.top();
		m_state_stack.pop();
	}

	struct stack_state
	{
		stack_state(parser_imp* imp) : m_imp(*imp) { m_imp.push_state(); }
		~stack_state() { m_imp.pop_state(); }

		parser_imp& m_imp;
	};

	struct lookahead_state
	{
		int m_lookahead;
		std::string m_token;
		std::stack<unicode> m_buffer;
	};

	void push_lookahead()
	{
		m_lookahead_stack.push({m_lookahead, std::move(m_token), std::move(m_buffer)});
		m_lookahead = xml_Undef;
		m_token.clear();
	}

	void pop_lookahead()
	{
		m_lookahead = m_lookahead_stack.top().m_lookahead;
		std::swap(m_lookahead_stack.top().m_token, m_token);
		std::swap(m_lookahead_stack.top().m_buffer, m_buffer);
		m_lookahead_stack.pop();
	}

	struct stack_lookahead
	{
		stack_lookahead(parser_imp* imp) : m_imp(*imp)
		{
			m_imp.push_lookahead();
		}
		~stack_lookahead()
		{
			m_imp.pop_lookahead();
		}
		parser_imp& m_imp;
	};

	// And during parsing we keep track of the namespaces we encounter.
	struct ns_state
	{
		ns_state(parser_imp *imp)
			: m_parser_imp(imp), m_next(imp->m_ns)
		{
			m_parser_imp->m_ns = this;
		}

		~ns_state()
		{
			m_parser_imp->m_ns = m_next;
		}

		parser_imp *m_parser_imp;
		std::string m_default_ns;
		ns_state *m_next;

		std::map<std::string, std::string>
			m_known;

		std::string default_ns()
		{
			std::string result = m_default_ns;
			if (result.empty() and m_next != nullptr)
				result = m_next->default_ns();
			return result;
		}

		std::string ns_for_prefix(const std::string& prefix)
		{
			std::string result;

			if (m_known.find(prefix) != m_known.end())
				result = m_known[prefix];
			else if (m_next != nullptr)
				result = m_next->ns_for_prefix(prefix);

			return result;
		}
	};

	bool is_char(unicode uc)
	{
		return
			m_state.m_version == 1.0 ?
				uc == 0x09 or
				uc == 0x0A or
				uc == 0x0D or
				(uc >= 0x020 and uc <= 0x0D7FF) or
				(uc >= 0x0E000 and uc <= 0x0FFFD) or
				(uc >= 0x010000 and uc <= 0x010FFFF) :

				// or 1.1?				
				
				uc == 0x09 or
				uc == 0x0A or
				uc == 0x0D or
				(uc >= 0x020 and uc < 0x07F) or
				uc == 0x085 or
				(uc >= 0x0A0 and uc <= 0x0D7FF) or
				(uc >= 0x0E000 and uc <= 0x0FFFD) or
				(uc >= 0x010000 and uc <= 0x010FFFF)
				;
	}

	bool is_referrable_char(unicode charref)
	{
		return
			m_state.m_version == 1.0 ?
				charref == 0x09 or
				charref == 0x0A or
				charref == 0x0D or
				(charref > 0x01F and charref < 0x0D800) or
				(charref > 0x0DFFF and charref < 0x0FFFE) or
				(charref > 0x0FFFF and charref < 0x00110000) :

				// 1.1
				(charref > 0x0 and charref < 0x0D800) or
				(charref > 0x0DFFF and charref < 0x0FFFE) or
				(charref > 0x0FFFF and charref < 0x00110000)
				;
	}

	bool m_validating;
	bool m_has_dtd;
	int m_lookahead;
	std::stack<std::unique_ptr<data_source>> m_source;

	parser_state m_state;
	std::stack<parser_state> m_state_stack;

	std::stack<lookahead_state> m_lookahead_stack;
	std::stack<unicode> m_buffer;
	std::string m_token;
	bool m_standalone;
	parser& m_parser;
	ns_state* m_ns;

	std::string m_root_element;
	doctype::entity_list m_parameter_entities;
	doctype::entity_list m_general_entities;
	doctype::element_list m_doctype;

	std::set<std::string> m_notations;
	std::set<std::string> m_ids;			// attributes of type ID should be unique
	std::set<std::string> m_unresolved_ids; // keep track of IDREFS that were not found yet
};

