// some inlines

inline void parser_imp::s(bool at_least_one)
{
	if (at_least_one)
		match(xml_Space);

	while (m_lookahead == xml_Space)
		match(xml_Space);
}

inline void parser_imp::eq()
{
	s();
	match('=');
	s();
}

